public class Main{
    public static void main(String[] args){
        
        int num = 4, num1 = 6;
        System.out.println(Math.max(num, num1));
        System.out.println(Math.min(num, num1));
        System.out.println(Math.sqrt(36));
        System.out.println(Math.abs(-36));
        System.out.println(Math.abs(5));
        System.out.println(Math.random()); // between 0 and 1
        System.out.println(4+(8-4)*Math.random());  // between 4 and 8
        
    }
}
