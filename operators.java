public class main{
    public static void main(String[] args){
        
        //comparison
        
        int num1 = 45, num2 = 5;
        num1 += 3;
        num2 -= 3;
        
        System.out.print("The walue of num1 + num2 is:");
        System.out.println(num1 + num2);
        
        System.out.print("The walue of num1 - num2 is:");
        System.out.println(num1 - num2);
        
        System.out.print("The walue of num1 * num2 is:");
        System.out.println(num1 * num2);
        
        System.out.print("The walue of num1 / num2 is:");
        System.out.println(num1 / num2);
        
        System.out.print("The walue of num1 % num2 is:");
        System.out.println(num1 % num2);
        
        //assingment
        
        System.out.println(num1++);
        System.out.println(++num1);
        System.out.println(num1--);
        System.out.println(--num1);

    }
}

